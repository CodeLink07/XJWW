  function expandData(compressed) {
    const result = [];
    for (let i = 0; i < compressed.length; i++) {
      const [score, count, cumulative] = compressed[i];
      if (i < compressed.length - 1) {
        const nextScore = compressed[i + 1][0];
        if (score - nextScore === 1) {
          result.push({ score, count, cumulative });
        } else if (score - nextScore > 1) {
          let currentCumulative = cumulative;
          for (let s = score; s > nextScore; s--) {
            if (s === score) {
              result.push({ score: s, count, cumulative: currentCumulative });
            } else {
              result.push({ score: s, count: 0, cumulative: currentCumulative });
            }
          }
        }
      } else {
        result.push({ score, count, cumulative });
      }
    }
    return result.reverse();
  }

